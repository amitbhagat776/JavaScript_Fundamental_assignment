// Q5. Write a JavaScript program that calculates the simple interest using the formula 
// Simple interest = (principal * rate * time) / 100.

let principalValue = 2300  //principal amount
let interestRate = 20  //interest rate
let time = 2  // time in years

const simpleInterest = (principalValue * interestRate * time) / 100;
console.log(`The Simple interest is: ${simpleInterest}`);